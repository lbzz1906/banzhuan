package com.sys.pojo;

public class Shops {

    private Integer shopsId;

    private Integer usersId;

    private Integer purchaseId;

    private String shopsName;

    private String shopsAddress;

    private String shopsPhone;

    private String shopsEmail;

    private String usersName;

    public Integer getShopsId() {
        return shopsId;
    }

    public void setShopsId(Integer shopsId) {
        this.shopsId = shopsId;
    }

    public Integer getUsersId() {
        return usersId;
    }

    public void setUsersId(Integer usersId) {
        this.usersId = usersId;
    }

    public Integer getPurchaseId() {
        return purchaseId;
    }

    public void setPurchaseId(Integer purchaseId) {
        this.purchaseId = purchaseId;
    }

    public String getShopsName() {
        return shopsName;
    }

    public void setShopsName(String shopsName) {
        this.shopsName = shopsName;
    }

    public String getShopsAddress() {
        return shopsAddress;
    }

    public void setShopsAddress(String shopsAddress) {
        this.shopsAddress = shopsAddress;
    }

    public String getShopsPhone() {
        return shopsPhone;
    }

    public void setShopsPhone(String shopsPhone) {
        this.shopsPhone = shopsPhone;
    }

    public String getShopsEmail() {
        return shopsEmail;
    }

    public void setShopsEmail(String shopsEmail) {
        this.shopsEmail = shopsEmail;
    }

    public String getUsersName() {
        return usersName;
    }

    public void setUsersName(String usersName) {
        this.usersName = usersName;
    }
}